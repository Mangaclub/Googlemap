class GmapModelgm_kmls extends JModelList
{

	protected function getListQuery()
	{
		// Create a new query object.
		$db = JFactory::getDBO();
		$query = $db->getQuery(true);

		// Select some fields
		$query->select('*');

		// From the gmap_gmaps table
		$query->from('#__gm_kml');

		return $query;
	}

